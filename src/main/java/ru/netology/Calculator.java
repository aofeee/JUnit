package ru.netology;

public class Calculator {
    public static int sum(int x, int y) {
        return x + y;
    }

    public static int div(int x, int y) {
        return x / y;
    }

    public static int multiply(int x, int y) {
        return x * y;
    }

    public static int sub(int x, int y) { return x - y; }
}